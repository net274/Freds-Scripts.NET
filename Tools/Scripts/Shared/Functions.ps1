function CheckExitCode($msg)
{
	if ($LastExitCode -ne 0)
	{
		Write-Error $msg
		Exit 1
	}
}
#==================================================================================================
function FullPath($path)
{
	$location = $(Get-Location)
	return "$($location)\$($path)"
}
#==================================================================================================
function SetFolder($scriptPath)
{
	Set-Location "${scriptPath}\..\.."
}
